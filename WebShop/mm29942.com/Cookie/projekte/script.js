function CShow()
{
    document.getElementById('colorIMG').style.visibility = 'visible';
    document.getElementById('all').style.visibility = 'hidden';
    document.getElementById('btnC2').style.visibility = 'visible';
}
function CHide()
{
    document.getElementById('colorIMG').style.visibility = 'hidden';
    document.getElementById('all').style.visibility = 'visible';
    document.getElementById('btnC2').style.visibility = 'hidden';
}